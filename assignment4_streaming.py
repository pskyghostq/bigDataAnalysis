# 실험 실행 함수
# ══════════════════════════════════════════════════════════════════════
def run_bloom_filter_experiments(filename, true_set, total_rows):
    print("\n=== 블룸 필터 실험 ===")
    configs = [(50_000, 3), (100_000, 4), (200_000, 5), (500_000, 7)]
    results = []

    # 질의 데이터 구성: 실제 존재 항목 500개 + 존재하지 않는 항목 500개
    pos_samples = random.sample(sorted(true_set), 500)
    neg_samples = [f"fake_item_{i}" for i in range(500)]
    query_items  = pos_samples + neg_samples
    query_labels = [True] * 500 + [False] * 500

    for size, nhash in configs:
        bf = BloomFilter(size, nhash)

        t0 = time.perf_counter()
        with open(filename) as f:
            for row in csv.DictReader(f):
                bf.add(row["user_id"])
                bf.add(row["movie_id"])
        elapsed = time.perf_counter() - t0

        tp = fp = tn = fn = 0

        for item, label in zip(query_items, query_labels):
            pred = item in bf

            if label and pred:
                tp += 1
            elif label and not pred:
                fn += 1
            elif not label and pred:
                fp += 1
            else:
                tn += 1

        fpr  = fp / (fp + tn) if (fp + tn) > 0 else 0
        mem  = bf.memory_bytes()
        tput = total_rows / elapsed

        row = dict(
            size=size,
            nhash=nhash,
            tp=tp,
            fp=fp,
            fn=fn,
            tn=tn,
            fpr=fpr,
            mem_kb=mem / 1024,
            time=elapsed,
            throughput=tput
        )

        results.append(row)

        print(
            f"  size={size:>7}, k={nhash}: "
            f"FPR={fpr:.4f} "
            f"mem={mem/1024:.0f}KB "
            f"time={elapsed:.3f}s "
            f"tput={tput:.0f}/s"
        )

    return results


def run_cms_experiments(filename, true_freq, total_rows):
    print("\n=== Count-Min Sketch 실험 ===")

    configs = [(500, 3), (1000, 4), (2000, 5), (5000, 7)]
    top_movies = sorted(true_freq.items(), key=lambda x: -x[1])[:100]

    results = []

    for width, depth in configs:
        cms = CountMinSketch(width, depth)

        t0 = time.perf_counter()

        with open(filename) as f:
            for row in csv.DictReader(f):
                cms.add(row["movie_id"])

        elapsed = time.perf_counter() - t0

        errors = [
            abs(cms.query(mid) - cnt) / cnt
            for mid, cnt in top_movies
        ]

        mean_err = sum(errors) / len(errors)
        max_err  = max(errors)
        mem      = cms.memory_bytes()
        tput     = total_rows / elapsed

        row = dict(
            width=width,
            depth=depth,
            mean_rel_err=mean_err,
            max_rel_err=max_err,
            mem_kb=mem / 1024,
            time=elapsed,
            throughput=tput
        )

        results.append(row)

        print(
            f"  w={width:>5}, d={depth}: "
            f"mean_err={mean_err:.4f} "
            f"max_err={max_err:.4f} "
            f"mem={mem/1024:.0f}KB "
            f"time={elapsed:.3f}s "
            f"tput={tput:.0f}/s"
        )

    return results


