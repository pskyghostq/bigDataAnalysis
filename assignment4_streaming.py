# 블룸 필터 (Bloom Filter)
# ══════════════════════════════════════════════════════════════════════
class BloomFilter:
    """
    확률적 집합(Set) 포함 여부 확인 자료구조
    - False Negative(거짓 음성): 절대 발생하지 않음
    - False Positive(거짓 양성): 발생 가능 (크기와 해시 함수 수에 따라 결정)
    - 메모리 복잡도: O(size) 비트
    """
    def __init__(self, size: int, num_hashes: int):
        self.size       = size
        self.num_hashes = num_hashes
        self.bit_array  = bytearray(size)   # 슬롯당 1바이트 사용
        self.count      = 0

    def _hashes(self, item):
        """num_hashes 개의 독립적인 해시값 생성"""
        item = str(item).encode()
        for i in range(self.num_hashes):
            h = int(hashlib.md5(item + i.to_bytes(2, 'big')).hexdigest(), 16)
            yield h % self.size

    def add(self, item):
        """항목 삽입"""
        for h in self._hashes(item):
            self.bit_array[h] = 1
        self.count += 1

    def __contains__(self, item):
        """조회: True → 존재할 가능성 있음 / False → 확실히 존재하지 않음"""
        return all(self.bit_array[h] for h in self._hashes(item))

    def memory_bytes(self) -> int:
        return sys.getsizeof(self.bit_array)

    @staticmethod
    def optimal_k(m: int, n: int) -> int:
        """이론적으로 최적의 해시 함수 개수 계산"""
        return max(1, round((m / n) * math.log(2)))

    @staticmethod
    def expected_fpr(m: int, k: int, n: int) -> float:
        """이론적 False Positive Rate 계산"""
        return (1 - math.exp(-k * n / m)) ** k


