# 카운트-민 스케치 (Count-Min Sketch)
# ══════════════════════════════════════════════════════════════════════
class CountMinSketch:
    """
    확률적 빈도 추정 자료구조
    - 실제 값보다 작게 추정하지 않음 (과대 추정만 가능)
    - 오차 보장:
      P(estimate > true + eps*N) < delta
      여기서 eps = e/width, delta = (1/e)^depth
    - 메모리 복잡도: O(width * depth) 정수
    """
    def __init__(self, width: int, depth: int):
        self.width = width
        self.depth = depth
        self.table = [[0] * width for _ in range(depth)]
        self.seeds = [random.randint(0, 2**31) for _ in range(depth)]
        self.total = 0

    def _hashes(self, item):
        """depth 개의 독립적인 해시값 생성"""
        item = str(item).encode()
        for i, seed in enumerate(self.seeds):
            h = int(hashlib.md5(item + seed.to_bytes(4, 'big')).hexdigest(), 16)
            yield h % self.width

    def add(self, item, count: int = 1):
        """항목 빈도 증가"""
        for i, h in enumerate(self._hashes(item)):
            self.table[i][h] += count
        self.total += count

    def query(self, item) -> int:
        """항목 빈도 추정값 반환 (각 행의 최소값 사용)"""
        return min(self.table[i][h] for i, h in enumerate(self._hashes(item)))

    def memory_bytes(self) -> int:
        return sys.getsizeof(self.table) + self.depth * self.width * 8

    def error_bound(self) -> float:
        """이론적 오차 상한 계산"""
        return math.e * self.total / self.width


