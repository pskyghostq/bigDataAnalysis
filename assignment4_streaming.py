# 데이터셋 생성 (한 번만 실행하여 events.csv 생성)
# ══════════════════════════════════════════════════════════════════════
def generate_dataset(filename="events.csv", n=200_000):
    movies  = [f"movie_{i}" for i in range(1, 4000)]
    users   = [f"user_{i}"  for i in range(1, 10000)]
    actions = ["view", "rate", "like", "skip", "watchlist"]

    with open(filename, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["event_id", "user_id", "movie_id", "action", "rating"])
        for i in range(n):
            uid    = random.choices(users,  weights=[1/(j+1) for j in range(len(users))])[0]
            mid    = random.choices(movies, weights=[1/(j+1) for j in range(len(movies))])[0]
            act    = random.choices(actions, weights=[5,3,2,2,1])[0]
            rating = random.randint(1,5) if act == "rate" else ""
            w.writerow([i+1, uid, mid, act, rating])
    print(f"데이터셋 생성 완료: {n}개 레코드 → {filename}")


