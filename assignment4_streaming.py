# 실제 정답(Ground Truth) 계산
# ══════════════════════════════════════════════════════════════════════
def compute_ground_truth(filename="events.csv"):
    true_set  = set()
    true_freq = defaultdict(int)
    total     = 0

    t0 = time.perf_counter()
    with open(filename) as f:
        for row in csv.DictReader(f):
            true_set.add(row["user_id"])
            true_set.add(row["movie_id"])
            true_freq[row["movie_id"]] += 1
            total += 1
    elapsed = time.perf_counter() - t0

    mem = sys.getsizeof(true_set) + sum(
        sys.getsizeof(k) + sys.getsizeof(v) for k, v in true_freq.items()
    )
    return true_set, true_freq, total, elapsed, mem


